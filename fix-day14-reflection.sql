Watching the kids debate experiment ideas at the Seder. The afikomen hypothesis, the argument about correlation vs causation, the older kid dismissing his own data — that conversation was the highlight of the entire two weeks. Getting people to think like experimenters is the whole point. If this class does nothing else but make people ask "how would we test that?" at dinner, it was worth taking.

