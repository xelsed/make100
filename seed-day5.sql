## Results

The Haribo bear burned **much faster** than the Sour Patch worm. It also seemed to have a **lower melting temperature** — it started deforming and dripping sooner, before it even caught flame. The Sour Patch worm held its shape longer and took about 50 seconds to fully burn. The Haribo bear went noticeably quicker.

## Why?

My guess: different sugar composition and gelatin density. Sour Patch gummies have a sour sugar coating that might act as a brief insulator, and they seem to have a firmer texture (more gelatin per volume). Haribo bears are softer and melt faster, which means they hit combustion temperature sooner.

