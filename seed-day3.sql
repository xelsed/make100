## Experiment A: Cortical Labs — Watching Neurons Learn Pong

I got access to Cortical Labs'' DishBrain platform — actual biological neurons on a chip learning to play Pong. Spent an hour and 25 minutes watching it.

They did smart things with the training setup, like dynamically growing and shrinking the paddle size to adjust difficulty. But in over an hour of watching, I did not see any apparent learning. The neurons were firing, the paddle was moving, but it was not meaningfully improving at the game.

This was frustrating but also kind of the point — biological learning is slow. We are used to ML models that converge in minutes. Actual neurons take their time. Will keep watching in future sessions to see if longer exposure produces visible improvement.

## Experiment B: How Long to Completely Burn a Sour Patch Gummy Worm

This did not start as a planned experiment. It started because the Cortical Labs session did not feel *experimenty* enough — I was just watching a screen. I wanted to do something with my hands. Something with a measurable outcome.

**The question:** If you hold a Sour Patch gummy worm above a stove flame, how long does it take to completely burn through all its energy?

**Method:** Hold gummy worm with tongs above a gas stove flame. Time from ignition to complete burnout. Repeat with a second worm to get an average.

**Results:**
- Worm 1: ~50 seconds
- Worm 2: ~52 seconds
- **Average: ~51 seconds**

The sugar catches first, caramelizes, then the gelatin structure collapses and the whole thing burns like a tiny torch. The flame color shifts from blue (stove gas) to orange-yellow (sugar combustion) during the process.

## Takeaway

Now I know it takes about 50 seconds to completely burn a gummy worm. Perhaps one day that will save someone''s life.

The real lesson: when the high-tech experiment does not feel like enough, sometimes the best move is to literally set something on fire.',
