  '## The Experiment

Brought my daughter Julia to visit ITP with me today. The question: how would a gifted 13-year-old respond to three very different graduate-level classes?

## The Results

### Vibe Coding
She did not pay enough attention here. People heads-down in editors, the energy was focused but not visually legible from the outside. Not enough to pull her in.

### Inventions
She liked it — the physical objects, the prototypes, things she could see and touch. But she stayed at a distance. Interested, not engaged.

### Democracy and AI
Fully engaged. And not just passively — **Julia contributed meaningfully to the class discussion.** She had opinions, she asked questions, she was in it. This is the one I predicted she would connect with, and she proved the theory right.

## What This Proved

A gifted 13-year-old can hold her own in a graduate school environment when the subject matter is accessible and discussion-based. The barrier is not intelligence or maturity — it is format. Give her a conversation about real stakes (democracy, AI, power) and she is right there with the grad students.

The most technically impressive class was the least engaging for her. The most conceptually accessible one pulled her all the way in. Kids engage with technology through **meaning first, mechanism second**.

## Takeaway

This experiment proved that age is not the bottleneck for intellectual engagement. Context and accessibility are. If I want to get Julia interested in what I am doing at ITP, I should lead with the *why* not the *how*. Show her the questions, not the code.',
